package com.youtube.ecommerce.entity;

import javax.persistence.*;

@MappedSuperclass
public class StringResult {
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
